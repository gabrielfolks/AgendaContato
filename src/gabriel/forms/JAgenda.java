//Para não encher os Botões com linhas de comando, crio metodos que se comunicam
//com o Banco e quando levados p/ os Botões devolvem uma Resposta
    protected void listarContatos() throws SQLException {
        ContatoDao Dao = new ContatoDao(); //Cria objeto da Classe ContatoDao
//Pega o q a pessoa digitou no campo pesquisa do Jframe, procura no Banco e retorna
//para o objeto List "contatos"
        contatos = Dao.getLista("%" + jTpesquisa.getText() + "%");
        mostrarPesquisa(contatos);//metodo que mostra os dados retornados pro List
        // na tabela

    }

    public void efetuarCadastro() {
        try {
            Contato c1 = new Contato(); //Instancia um cadastro
            c1.setNome(jTnome.getText()); //pega o que esta no campo do Jframe e
            //seta no argumento(variavel nome) referente..
            c1.setEndereco(jTend.getText());
            c1.setSexo(jTsex.getText());
            c1.setTelefone(jTtel.getText());
            c1.setEmail(jTema.getText());
            ContatoDao Dao = new ContatoDao();//chama Classe Banco

            Dao.adiciona(c1);// e usa o metodo para adicionar
        } catch (SQLException ex) {
            Logger.getLogger(JAgenda.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void mostrarPesquisa(List<Contato> contatos) {//Não se comunica com o Banco
        while (tmcontato.getRowCount() > 0) {//Todo o processo é para ajustar os valores
            tmcontato.removeRow(0); //de pesquisa em uma tabela
        }
        if (contatos.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Nenhum contato cadastrado!");

        } else {
            String[] linha = new String[]{null, null, null, null, null, null};
            for (int i = 0; i < contatos.size(); i++) {
                tmcontato.addRow(linha);
                tmcontato.setValueAt(contatos.get(i).getId(), i, 0);
                tmcontato.setValueAt(contatos.get(i).getNome(), i, 1);
                tmcontato.setValueAt(contatos.get(i).getEndereco(), i, 2);
                tmcontato.setValueAt(contatos.get(i).getSexo(), i, 3);
                tmcontato.setValueAt(contatos.get(i).getTelefone(), i, 4);
                tmcontato.setValueAt(contatos.get(i).getEmail(), i, 5);
            }
        }
    }

    private void jTTableLinhaSelecionada(JTable tabela) { 
        //seta os valores do List "contatos",nos os campos do Jframe 
        //é necessario passar um código padrão da tabela em GeneratedCode->InitComponents
        //Inserção do código em Projeto Jframe ->Propriedades->Codigo->codigo de pós-inicialização
        HabiliteCampos();                  
        if (Tabelapai.getSelectedRow() != -1) {
            jTid.setText(String.valueOf(contatos.get(tabela.getSelectedRow()).getId()));
            jTnome.setText(contatos.get(tabela.getSelectedRow()).getNome());
            jTend.setText(contatos.get(tabela.getSelectedRow()).getEndereco());
            jTsex.setText(contatos.get(tabela.getSelectedRow()).getSexo());
            jTtel.setText(contatos.get(tabela.getSelectedRow()).getTelefone());
            jTema.setText(contatos.get(tabela.getSelectedRow()).getEmail());
        } else {
            jTid.setText("");
            jTnome.setText("");
            jTend.setText("");
            jTsex.setText("");
            jTtel.setText("");
            jTema.setText("");
        }

    }

    public void excluirContato() throws SQLException {
        int pergunta = JOptionPane.showConfirmDialog(this, "Deseja excluir?", "Confirmação", JOptionPane.YES_NO_OPTION);
        if (pergunta == JOptionPane.YES_NO_OPTION) {
            ContatoDao Dao = new ContatoDao();
            Dao.Remove(contatos.get(Tabelapai.getSelectedRow()));
            mostrarPesquisa(contatos);
        }
    }

    public void aterarContato() throws SQLException {
        if (Tabelapai.getSelectedRow() != -1) {
            if (VerifiqueCampoVazio()) {
                Contato c1 = new Contato();
                ContatoDao Dao = new ContatoDao();

                c1.setId(Integer.valueOf(jTid.getText()));
                c1.setNome(jTnome.getText());
                c1.setEndereco(jTend.getText());
                c1.setSexo(jTsex.getText());
                c1.setTelefone(jTtel.getText());
                c1.setEmail(jTema.getText());
                Dao.Altera(c1);
                JOptionPane.showMessageDialog(null, "Alterado com sucesso!");
            }
        }
    }

