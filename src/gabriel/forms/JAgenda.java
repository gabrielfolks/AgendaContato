    public boolean VerifiqueCampoVazio() {//Metodo p/tratamento de campo importante nao preenchido
        if (!jTnome.getText().equals("") && !jTend.getText().equals("")) {
            return true;
        } else {
            JOptionPane.showMessageDialog(null, "Calmpos 'Nome' e 'Tel' nao preenchidos!");
            return false;
        }
    }

